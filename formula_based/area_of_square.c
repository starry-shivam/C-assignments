// calculate area of square

#include<stdio.h>
int main()
{
     // Declaration
     int side,squ_area;
     // taking input
     printf("Enter Side : ");
     scanf("%d",&side);
     // calculation
     squ_area=side*side;
     // and output
     printf("\nArea of Square = %d",squ_area);
     return 0;
}
